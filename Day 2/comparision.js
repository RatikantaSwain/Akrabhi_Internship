// equal operator
console.log(2 == 2); 
console.log(2 == '2'); 

// not equal operator
console.log(3 != 2); 
console.log('hello' != 'Hello'); 

// strict equal operator
console.log(2 === 2); 
console.log(2 === '2'); 

// strict not equal operator
console.log(2 !== '2'); 
console.log(2 !== 2); 